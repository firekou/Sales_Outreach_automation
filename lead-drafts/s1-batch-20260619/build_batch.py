def load_real_fields():
    """從既有抓取名單回查每位 Lead 的真實欄位。"""
    found = {}
    with open(LEADS_CSV, encoding="utf-8-sig") as f:
        for d in csv.DictReader(f):
            if d["name"] in BATCH:
                found[d["name"]] = d
    missing = set(BATCH) - set(found)
    if missing:
        raise SystemExit(f"❌ 名單中找不到：{missing}")
    return found


def char_len(s):
    return len(s.replace("\n", ""))


